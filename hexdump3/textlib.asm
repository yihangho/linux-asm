section .text

GLOBAL ClearLine, DumpChar, PrintLine

ClearLine:
	pushad
	mov edx, 15
.poke:
	mov eax, 0
	call DumpChar
	sub edx, 1
	jae .poke
	popad
	ret

DumpChar:
	push ebx
	push ecx
	
	mov bl, byte[DotXlat+eax]
	mov byte[ASCLin+edx+1], bl

	and eax, 0xFF
	mov ebx, eax
	lea ecx, [edx*2+edx]

	and eax, 0xF
	mov al, byte[HexDigits+eax]
	mov byte[DumpLin+ecx+2], al

	shr ebx, 4
	mov bl, byte[HexDigits+ebx]
	mov byte[DumpLin+ecx+1], bl

	pop ecx
	pop ebx
	ret

PrintLine:
	pushad
	mov eax, 4
	mov ebx, 1
	mov ecx, DumpLin
	mov edx, FULLLEN
	int 0x80
	popad
	ret
